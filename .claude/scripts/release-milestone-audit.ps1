<#
release-milestone-audit.ps1 — pre-release milestone tidiness audit.

Reports two lists the orchestrator's `/release-milestone-check` skill
asks the user to resolve before ticking checklist item 3:

  1. Open issues + PRs on the release milestone (anything that could
     land late and bypass the release-prep public-API / baselines /
     release-notes pass).
  2. Open PRs on `linq2db.baselines` for source PRs that already merged
     into the milestone. These are auto-generated by CI per source PR
     and don't auto-close when the source merges — they accrue and
     contaminate baselines if not merged before release.

Read-only — never closes, merges, or edits anything. The orchestrator
surfaces results and asks the user to act.

Action:

  audit       Inputs:  -Milestone <ver>  [-PrepPR <n>]
                       [-Repo <owner/repo>]               (default: linq2db/linq2db)
                       [-BaselinesRepo <owner/repo>]      (default: linq2db/linq2db.baselines)
              Output:  {
                ok, milestone: { title, number, open_issues, closed_issues, html_url },
                openIssues[], openPRs[], mergedPRs[], staleBaselinesPRs[],
                counts: { openIssues, openPRs, staleBaselines }
              }

Conventions: `.claude/docs/script-authoring.md`.
#>

[CmdletBinding()]
param(
    [Parameter(Mandatory)][ValidateSet('audit')]
    [string]$Action,

    [Parameter(Mandatory)][string]$Milestone,
    [int]$PrepPR,
    [string]$Repo = 'linq2db/linq2db',
    [string]$BaselinesRepo = 'linq2db/linq2db.baselines'
)

$global:ScriptBaseName = 'release-milestone-audit'
. (Join-Path $PSScriptRoot '_shared.ps1')

# -- milestone lookup --------------------------------------------------------

function Get-Milestone {
    param([string]$Title, [string]$Repo)
    # gh api with --paginate guarantees we see all open milestones even if the
    # project ever exceeds 30 per page. We need an exact title match (not
    # `--search`, which is fuzzy).
    $r = Invoke-GhJson -ArgumentList @(
        'api', "repos/$Repo/milestones?state=all&per_page=100", '--paginate'
    )
    if (-not $r.ok) { Exit-WithError "milestone lookup failed: $($r.error)" }
    foreach ($m in $r.data) {
        if ($m.title -eq $Title) { return $m }
    }
    return $null
}

# -- issue / PR listing ------------------------------------------------------

# `gh issue list` returns BOTH issues and PRs when querying by milestone
# (PRs are issues under the hood). We separate them via the pull_request
# field, which is present (object) on PRs and null on issues.
function Get-MilestoneItems {
    param([string]$Repo, [int]$MilestoneNumber, [string]$State)
    $r = Invoke-GhJson -ArgumentList @(
        'api',
        "repos/$Repo/issues?milestone=$MilestoneNumber&state=$State&per_page=100",
        '--paginate'
    )
    if (-not $r.ok) { Exit-WithError "milestone items lookup failed: $($r.error)" }
    return $r.data
}

# Merged-PR list specifically. `gh issue list ... --state closed` returns
# both closed-issues and merged-PRs; we filter to PRs whose `merged_at` is
# set. (Closed-but-not-merged PRs aren't relevant for baselines audit —
# their baselines branches were cleaned up when the source PR closed.)
function Get-MergedMilestonePRs {
    param([string]$Repo, [int]$MilestoneNumber)
    $closed = Get-MilestoneItems -Repo $Repo -MilestoneNumber $MilestoneNumber -State 'closed'
    $merged = New-Object 'System.Collections.Generic.List[object]'
    foreach ($it in @($closed)) {
        if (-not $it.pull_request) { continue }   # issue, not PR
        # `repos/.../issues` doesn't include merged_at directly — but
        # pull_request.merged_at is populated for merged PRs.
        if (-not $it.pull_request.merged_at) { continue }
        $merged.Add([ordered]@{
            number     = $it.number
            title      = $it.title
            html_url   = $it.html_url
            merged_at  = $it.pull_request.merged_at
            user       = if ($it.user) { $it.user.login } else { $null }
        }) | Out-Null
    }
    return $merged.ToArray()
}

# -- baselines stale-PR detection --------------------------------------------

# Linq2db.baselines auto-generates one PR per source-repo PR. Head ref
# naming convention is `baselines/pr_<N>` (per `.claude/docs/pr-and-push.md`).
# We list all open PRs on the baselines repo, then match head refs against
# the set of merged-milestone source PR numbers.
function Get-OpenBaselinesPRs {
    param([string]$Repo)
    $r = Invoke-GhJson -ArgumentList @(
        'api',
        "repos/$Repo/pulls?state=open&per_page=100",
        '--paginate'
    )
    if (-not $r.ok) { Exit-WithError "baselines PR list failed: $($r.error)" }
    return $r.data
}

function Find-StaleBaselinesPRs {
    param($BaselinesPRs, [int[]]$MergedSourcePRNumbers)
    $mergedSet = [System.Collections.Generic.HashSet[int]]::new()
    foreach ($n in $MergedSourcePRNumbers) { [void]$mergedSet.Add($n) }
    $stale = New-Object 'System.Collections.Generic.List[object]'
    foreach ($p in @($BaselinesPRs)) {
        $headRef = $p.head.ref
        if (-not $headRef) { continue }
        # Match both `baselines/pr_<n>` (canonical) and `baselines/pull_<n>`
        # (defensive — older CI runs may have used a different scheme).
        if ($headRef -match '^baselines/p(?:r|ull)_([0-9]+)$') {
            $sourcePr = [int]$Matches[1]
            if ($mergedSet.Contains($sourcePr)) {
                $stale.Add([ordered]@{
                    number       = $p.number
                    headRef      = $headRef
                    sourcePR     = $sourcePr
                    title        = $p.title
                    html_url     = $p.html_url
                    updated_at   = $p.updated_at
                }) | Out-Null
            }
        }
    }
    return $stale.ToArray()
}

# -- audit (main) ------------------------------------------------------------

function Do-Audit {
    $m = Get-Milestone -Title $Milestone -Repo $Repo
    if (-not $m) { Exit-WithError "milestone '$Milestone' not found in $Repo" }

    $openItems = Get-MilestoneItems -Repo $Repo -MilestoneNumber $m.number -State 'open'

    $openIssues = New-Object 'System.Collections.Generic.List[object]'
    $openPRs    = New-Object 'System.Collections.Generic.List[object]'
    foreach ($it in @($openItems)) {
        if ($PrepPR -and $it.number -eq $PrepPR) { continue }   # skip the prep PR itself
        $row = [ordered]@{
            number     = $it.number
            title      = $it.title
            html_url   = $it.html_url
            updated_at = $it.updated_at
            user       = if ($it.user) { $it.user.login } else { $null }
            labels     = @($it.labels | ForEach-Object { $_.name })
        }
        if ($it.pull_request) {
            $openPRs.Add($row) | Out-Null
        } else {
            $openIssues.Add($row) | Out-Null
        }
    }

    $merged = Get-MergedMilestonePRs -Repo $Repo -MilestoneNumber $m.number
    if ($PrepPR) {
        $merged = @($merged | Where-Object { $_.number -ne $PrepPR })
    }
    $mergedNumbers = @($merged | ForEach-Object { [int]$_.number })

    $baselinesPRs = Get-OpenBaselinesPRs -Repo $BaselinesRepo
    $stale = Find-StaleBaselinesPRs -BaselinesPRs $baselinesPRs -MergedSourcePRNumbers $mergedNumbers

    Write-JsonOutput @{
        ok        = $true
        action    = 'audit'
        milestone = @{
            title          = $m.title
            number         = $m.number
            state          = $m.state
            open_issues    = $m.open_issues
            closed_issues  = $m.closed_issues
            html_url       = $m.html_url
        }
        openIssues          = $openIssues.ToArray()
        openPRs             = $openPRs.ToArray()
        mergedPRs           = $merged
        staleBaselinesPRs   = $stale
        counts              = @{
            openIssues     = $openIssues.Count
            openPRs        = $openPRs.Count
            mergedPRs      = @($merged).Count
            staleBaselines = @($stale).Count
        }
    }
}

# -- dispatch ----------------------------------------------------------------

switch ($Action) {
    'audit' { Do-Audit }
    default { Exit-WithError "unknown action: $Action" }
}
