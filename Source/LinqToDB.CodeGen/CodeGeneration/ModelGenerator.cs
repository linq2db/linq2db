		private const string SCHEMAS_INIT_METHOD = "InitSchemas";
		private const string MAPPING_SCHEMA_INIT_METHOD = "InitMappingSchema";
		private const string DATA_CONTEXT_INIT_METHOD = "InitDataContext";

		public static string GenerateContextModel(DataContextModel model, CodeGenerationSettings settings, INameConverterProvider pluralizationProvider)
		{
			var langServices = new CSharpLanguageServices();
			var customEqualityTypes = new HashSet<IType>(new TypeComparer(true));
			foreach (var (typeName, isValueType) in settings.NonBooleanEqualityTypes)
			{
				customEqualityTypes.Add(TypeBuilder.ParseType(langServices, typeName, isValueType));
			}

			var codeModel = GenerateCodeModel(model, settings, new CSharpLanguageFeatures(), langServices, pluralizationProvider, customEqualityTypes);

			var knownTypes = new TypeCollectorVisitor(langServices);
			var namesNormalization = new CSharpNameNormalizationVisitor(langServices, knownTypes.LocalTypes, knownTypes.ExternalTypes);
			namesNormalization.Visit(codeModel);

			var namespaces = new CollectAllNamespacesVisitor(langServices);
			namespaces.Visit(codeModel);

			foreach (var ns in settings.ConflictingNamespaces)
			{
				// TODO: HACK: add separate method for namespace parsing as type parser allows too much
				var type = TypeBuilder.ParseType(langServices, ns, false);
				var nsName = new CodeIdentifier[type.Namespace?.Length ?? 0 + 1];
				nsName[nsName.Length - 1] = type.Name!;
				if (type.Namespace != null)
				{
					for (var i = 0; i < type.Namespace.Length; i++)
						nsName[i] = type.Namespace[i];
				}

				namespaces.Namespaces.Add(nsName);
			}


			CodeGenerationVisitor codeGenerator = new CSharpCodeGenerator(langServices, settings, namespaces.Namespaces);
			codeGenerator.Visit(codeModel);
			return codeGenerator.GetResult();
		}

		private static CodeFile GenerateCodeModel(
			DataContextModel model,
			CodeGenerationSettings settings,
			ILanguageFeatures langFeatures,
			ILanguageServices langServices,
			INameConverterProvider pluralizationProvider,
			ISet<IType> customEqualityTypes)
		{
			var codeModel = new CodeModelBuilder(langServices);
			var modelFile = codeModel.File(settings.ContextFileName, settings.ContextFolder);

			// autogeneration notice header
			// note that roslyn compiler disables NRT for files for backward compatibility and they should be re-enabled explicitly
			if (settings.MarkAsAutoGenerated)
			{
				modelFile.Add(codeModel.Commentary("---------------------------------------------------------------------------------------------------", false));
				modelFile.Add(codeModel.Commentary(" <auto-generated>", false));
				modelFile.Add(codeModel.Commentary("    This code was generated by T4Model template for T4 (https://github.com/linq2db/linq2db).", false));
				modelFile.Add(codeModel.Commentary("    Changes to this file may cause incorrect behavior and will be lost if the code is regenerated.", false));
				modelFile.Add(codeModel.Commentary(" </auto-generated>", false));
				modelFile.Add(codeModel.Commentary("---------------------------------------------------------------------------------------------------", false));
				modelFile.Add(codeModel.NewLine());
			}

			var disableXmlWarn = langFeatures.MissingXmlCommentWarnCode != null && settings.SuppressMissingXmlCommentWarnings;
			var nrtEnabled = langFeatures.SupportsNRT && settings.NullableReferenceTypes;

			// configure compiler options
			if (disableXmlWarn)
				modelFile.Add(codeModel.DisableWarnings(langFeatures.MissingXmlCommentWarnCode!));
			if (nrtEnabled && settings.MarkAsAutoGenerated)
				modelFile.Add(codeModel.EnableNullableReferenceTypes());
			if (disableXmlWarn || (nrtEnabled && settings.MarkAsAutoGenerated))
				modelFile.Add(codeModel.NewLine());

			// as at this point we should generate usings in file, we stop adding new items to file till we build
			// full code model and collect namespaces from it
			var @namespace = BuildModel(codeModel, model, settings, langServices, pluralizationProvider, customEqualityTypes);

			var usings = GetImports(@namespace, langServices);
			var linq2dbUsings = new List<CodeIdentifier[]>();
			foreach (var ns in usings.OrderBy(_ => _, CodeIdentifierComparer.Instance))
			{
				if (ns[0].Name == "LinqToDB")
					linq2dbUsings.Add(ns);
				else
					modelFile.Add(codeModel.Import(ns));
			}

			if (linq2dbUsings.Count > 0)
			{
				if (linq2dbUsings.Count < usings.Count)
					modelFile.Add(codeModel.NewLine());
				foreach (var ns in linq2dbUsings)
					modelFile.Add(codeModel.Import(ns));
			}

			if (usings.Count > 0)
				modelFile.Add(codeModel.NewLine());

			modelFile.Add(@namespace);

			return modelFile;
		}

		private static CodeElementNamespace BuildModel(
			CodeModelBuilder codeModel,
			DataContextModel model,
			CodeGenerationSettings settings,
			ILanguageServices langServices,
			INameConverterProvider pluralizationProvider,
			ISet<IType> customEqualityTypes)
		{
			var ns = codeModel.Namespace(settings.Namespace);

			GenerateContextClass(ns, codeModel, model, settings, langServices, pluralizationProvider, customEqualityTypes);

			return ns.Namespace;
		}

		private static void GenerateContextClass(
			NamespaceBuilder nsBuilder,
			CodeModelBuilder codeModel,
			DataContextModel model,
			CodeGenerationSettings settings,
			ILanguageServices langServices,
			INameConverterProvider pluralizationProvider,
			ISet<IType> customEqualityTypes)
		{
			var ctxBuilder = nsBuilder.Classes().New(codeModel.Identifier(model.DataContext.ClassName))
				.Public()
				.Partial();

			var contextTableProperties = ctxBuilder.Properties(true);
			var schemasRegion = ctxBuilder.Regions().New("Schemas");
			var contextConstructors = ctxBuilder.Constructors();
			var contextPartialMethods = ctxBuilder.Methods(true);
			var tableFunctions = ctxBuilder.Regions().New("Table Functions").Regions();

			var tupleClasses = nsBuilder.Classes();
			var entityClasses = nsBuilder.Classes();
			var extensions = nsBuilder.Classes();
			var schemas = nsBuilder.Classes();

			ClassBuilder? storedProcsClass = null;
			if (model.StoredProcedures.Count > 0)
				storedProcsClass = extensions.New(codeModel.Identifier(model.DataContext.ClassName + "StoredProcedures"))
					.Public()
					.Static()
					.Partial();

			ClassBuilder? scalarFunctionsClass = null;
			if (model.ScalarFunctions.Count > 0)
				scalarFunctionsClass = extensions.New(codeModel.Identifier("SqlFunctions"))
					.Public()
					.Static()
					.Partial();

			var columnMap = new Dictionary<ColumnModel, CodeProperty>();
			var entityBuilders = new Dictionary<EntityModel, ClassBuilder>();

			CodeIdentifier? initSchemasName = null;
			var schemaMap = new Dictionary<SchemaContextModel, (ClassGroup sprocClasses, ClassGroup schemaTupleClasses, ClassBuilder ctxClass, ClassBuilder schemaClass)>();

			if (model.SchemaContexts.Count > 0)
			{
				var schemaProperties = schemasRegion.Properties(true);
				var initMethod = schemasRegion.Methods(false).New(codeModel.Identifier(SCHEMAS_INIT_METHOD))
					.Public();
				var initMethodBody = initMethod.Body();
				initSchemasName = initMethod.Method.Name;

				foreach (var schema in model.SchemaContexts)
				{
					var schemaClassName = ContextModelBuilder.NormalizeIdentifier(
							settings.SchemaClassNameNormalization,
							schema.BaseTypeName,
							langServices,
							pluralizationProvider);
					var schemaPropertyName = ContextModelBuilder.NormalizeIdentifier(
							settings.SchemaPropertyNormalization,
							schema.BaseTypeName,
							langServices,
							pluralizationProvider);

					var schemaClass = schemas.New(codeModel.Identifier(schemaClassName))
						.Public()
						.Static()
						.Partial();
					var ctxClass = schemaClass.Classes()
						.New(codeModel.Identifier("DataContext"))
						.Public()
						.Partial();

					var entityProps = ctxClass.Properties(true);
					var fields = ctxClass.Fields(false);
					var ctxCtors = ctxClass.Constructors();

					var schemaProp = schemaProperties.New(codeModel.Identifier(schemaPropertyName), codeModel.Type(ctxClass.Type.Type, false))
						.Public()
						.Default(true);

					var ctxField = fields.New(codeModel.Identifier("_dataContext"), codeModel.Type(typeof(IDataContext), false))
						.Private()
						.ReadOnly();

					var ctorParam = codeModel.Parameter(codeModel.Type(typeof(IDataContext), false), codeModel.Identifier("dataContext"), Direction.In);
					ctxCtors.New()
						.Public()
						.Parameter(ctorParam)
						.Body()
						.Append(codeModel.Assign(ctxField.Field.Name, ctorParam.Name));

					initMethodBody.Append(
						codeModel.Assign(
							codeModel.Member(codeModel.This(), schemaProp.Property.Name),
							codeModel.New(ctxClass.Type.Type, new ICodeExpression[] { codeModel.This() }, Array.Empty<AssignExpression>())));

					var tuplesClassGroup = schemaClass.Classes();
					var entitiesClassGroup = schemaClass.Classes();
					var sprocClasses = schemaClass.Classes();
					var findMethodsGroup = schemaClass.Regions().New("Table Extensions").Methods(false);

					foreach (var entity in schema.Entities)
					{
						var entityClassBuilder = BuildEntity(model, settings, entitiesClassGroup, codeModel, entity, columnMap);
						entityBuilders.Add(entity, entityClassBuilder);

						var ctxProperty = entityProps.New(codeModel.Identifier(entity.NameInContext), codeModel.Type(typeof(ITable<>), false, entityClassBuilder.Type.Type))
							.Public();
						if (entity.Description != null)
							ctxProperty.XmlComment().Summary(entity.Description);
						ctxProperty
							.AddGetter()
							.Append(codeModel.Return(codeModel.ExtCall(
								codeModel.Type(typeof(DataExtensions), false),
								codeModel.Identifier(nameof(DataConnection.GetTable)),
								new[] { entityClassBuilder.Type.Type },
								new ICodeExpression[] { ctxField.Field.Name })));

						if (settings.GenerateFindExtensions)
							BuildFindExtension(codeModel, settings, langServices, pluralizationProvider, () => findMethodsGroup, entity, entityClassBuilder.Type.Type, customEqualityTypes);
					}

					schemaMap.Add(schema, (sprocClasses, tuplesClassGroup, ctxClass, schemaClass));
				}
			}

			if (settings.BaseContextClass != null)
				ctxBuilder.Inherits(codeModel.Type(settings.BaseContextClass, false));
			else
				ctxBuilder.Inherits(codeModel.Type(typeof(DataConnection), false));

			if (settings.IncludeDatabaseInfo)
			{
				var dbInfo = $@"
Database       : {model.DatabaseName}
Data Source    : {model.DataSource}
Server Version : {model.ServerVersion}";
				ctxBuilder.XmlComment()
					.Summary(dbInfo);
			}

			// constructors
			var configurationParam = codeModel.Parameter(codeModel.Type(typeof(string), false), codeModel.Identifier("configuration"), Direction.In);
			var optionsParam = codeModel.Parameter(codeModel.Type(typeof(LinqToDbConnectionOptions), false), codeModel.Identifier("options"), Direction.In);
			var typedOptionsParam = codeModel.Parameter(codeModel.Type(typeof(LinqToDbConnectionOptions<>), false, ctxBuilder.Type.Type), codeModel.Identifier("options"), Direction.In);
			var ctors = new []{
				contextConstructors.New().Public().Body(),
				contextConstructors.New()
					.Parameter(configurationParam)
					.Public()
					.Base(configurationParam.Name)
					.Body(),
				contextConstructors.New()
					.Parameter(optionsParam)
					.Public()
					.Base(optionsParam.Name)
					.Body(),
				contextConstructors.New()
					.Parameter(typedOptionsParam)
					.Public()
					.Base(typedOptionsParam.Name)
					.Body()
			};


			// methods
			var initDataContext = contextPartialMethods.New(codeModel.Identifier(DATA_CONTEXT_INIT_METHOD)).Partial();

			foreach (var body in ctors)
			{
				if (model.SchemaContexts.Count > 0)
					body.Append(codeModel.Call(codeModel.This(), initSchemasName!, Array.Empty<IType>(), Array.Empty<ICodeExpression>()));
				body.Append(codeModel.Call(codeModel.This(), initDataContext.Method.Name, Array.Empty<IType>(), Array.Empty<ICodeExpression>()));
			}

			MethodBuilder? initMappingSchema = null;
			MethodBuilder getInitMappingSchema()
			{
				if (initMappingSchema == null)
				{
					initMappingSchema = contextPartialMethods.New(codeModel.Identifier(MAPPING_SCHEMA_INIT_METHOD));
					foreach (var body in ctors)
						body.Append(codeModel.Call(codeModel.This(), initMappingSchema.Method.Name, Array.Empty<IType>(), Array.Empty<ICodeExpression>()));
				}

				return initMappingSchema;
			}

			// extension methods class
			ClassBuilder? extensionsClass = null;
			MethodGroup? findExtensions = null;
			RegionGroup? assocationExtensionsRegions = null;
			(ClassBuilder extensionsClass, MethodGroup findExtensions, RegionGroup associationExtensions) getExtClass()
			{
				if (extensionsClass == null)
				{
					extensionsClass = extensions.New(codeModel.Identifier("TableExtensions"))
						.Public()
						.Static()
						.Partial();
					findExtensions = extensionsClass.Methods(false);
					assocationExtensionsRegions = extensionsClass.Regions().New("Associations").Regions();
				}

				return (extensionsClass, findExtensions!, assocationExtensionsRegions!);
			}

			// entities
			var entities = new CodeClass[model.Entities.Count];
			var idx = 0;
			foreach (var entity in model.Entities.OrderBy(e => e.ClassName))
			{
				var entityClassBuilder = BuildEntity(model, settings, entityClasses, codeModel, entity, columnMap);

				var entityClass = entityClassBuilder.Type;
				entities[idx] = entityClass;
				idx++;
				entityBuilders.Add(entity, entityClassBuilder);

				var ctxProperty = contextTableProperties.New(codeModel.Identifier(entity.NameInContext), codeModel.Type(typeof(ITable<>), false, entityClass.Type))
					.Public();
				if (entity.Description != null)
					ctxProperty.XmlComment().Summary(entity.Description);
				ctxProperty
					.AddGetter()
					// it could be DataExtensions.GetTable or DataConnection.GetTable
					// we will generate DataExtensions.GetTable call into model for safety as we don't know if base type
					// is DataConnection-based in general
					.Append(codeModel.Return(
						codeModel.ExtCall(
							codeModel.Type(typeof(DataExtensions), false),
							codeModel.Identifier(nameof(DataExtensions.GetTable)),
							new[] { entityClass.Type },
							new ICodeExpression[] { codeModel.This() })));

				if (settings.GenerateFindExtensions)
					BuildFindExtension(codeModel, settings, langServices, pluralizationProvider, () => getExtClass().findExtensions, entity, entityClass.Type, customEqualityTypes);
			}

			if (settings.GenerateAssociations || settings.GenerateAssociationExtensions)
				BuildAssociations(codeModel, model, entityBuilders, columnMap, () => getExtClass().associationExtensions, settings, langServices, customEqualityTypes);

			// functions
			if (model.StoredProcedures.Count > 0
				|| model.ScalarFunctions.Count > 0
				|| model.TableFunctions.Count > 0
				|| model.Aggregates.Count > 0)
			{
				BuildTableFunctions(model, tableFunctions, model.TableFunctions, codeModel, settings, entityBuilders);
				if (model.StoredProcedures.Count > 0)
					BuildStoredProcedures(model, ctxBuilder.Type.Type, storedProcsClass!.Regions(), model.StoredProcedures, codeModel, settings, true, entityBuilders);
				if (model.ScalarFunctions.Count > 0)
					BuildScalarFunctions(model, ctxBuilder.Type.Type, scalarFunctionsClass!.Regions(), model.ScalarFunctions, codeModel, settings, tupleClasses, getInitMappingSchema);
				if (model.Aggregates.Count > 0)
					BuildAggregateFunctions(model, ctxBuilder.Type.Type, scalarFunctionsClass!.Regions(), model.Aggregates, codeModel, settings, true);
			}

			foreach (var schema in model.SchemaContexts)
			{
				var (sprocClasses, schemaTupleClasses, ctxClass, schemaClass) = schemaMap[schema];

				if (schema.TableFunctions.Count > 0)
				{
					var schemaTableFunctions = ctxClass.Regions().New("Table Functions").Regions();

					BuildTableFunctions(model, schemaTableFunctions, schema.TableFunctions, codeModel, settings, entityBuilders);
				}

				if (schema.StoredProcedures.Count > 0)
				{
					var extension = true;
					RegionGroup procs;
					if (settings.PutSchemaStoredProceduresToNestedClass)
					{
						procs = sprocClasses.New(codeModel.Identifier(model.DataContext.ClassName + "StoredProcedures"))
							.Public()
							.Static()
							.Partial()
							.Regions();
						extension = false;
					}
					else
					{
						procs = schemaClass.Regions().New("Stored Procedures").Regions();
					}

					BuildStoredProcedures(model, ctxBuilder.Type.Type, procs, schema.StoredProcedures, codeModel, settings, extension, entityBuilders);
				}

				if (schema.ScalarFunctions.Count > 0 || schema.Aggregates.Count > 0)
				{
					var extension = true;
					RegionGroup regions;
					if (settings.PutSchemaFunctionsToNestedClass)
					{
						regions = sprocClasses.New(codeModel.Identifier("SqlFunctions"))
							.Public()
							.Static()
							.Partial()
							.Regions();
						extension = false;
					}
					else
					{
						regions = schemaClass.Regions().New("Scalar/Aggregate Functions").Regions();
					}

					if (schema.ScalarFunctions.Count > 0)
						BuildScalarFunctions(model, ctxBuilder.Type.Type, regions, schema.ScalarFunctions, codeModel, settings, schemaTupleClasses, getInitMappingSchema);
					if (schema.Aggregates.Count > 0)
						BuildAggregateFunctions(model, ctxBuilder.Type.Type, regions, schema.Aggregates, codeModel, settings, extension);
				}
			}
		}

		private static void BuildAggregateFunctions(
			DataContextModel ctxModel,
			IType context,
			RegionGroup regions,
			List<AggregateModel> functions,
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			bool asExtension)
		{
			foreach (var func in functions)
			{
				var region = regions.New(func.MethodName);

				var method = region.Methods(false).New(codeModel.Identifier(func.MethodName))
					.Public()
					.Static();

				if (asExtension)
					method.Extension();

				var source = codeModel.TypeParameter(codeModel.Identifier("TSource"));
				method.TypeParameter(source);

				XmlCommentBuilder? xmlComment = null;
				if (func.Description != null)
				{
					xmlComment = method.XmlComment().Summary(func.Description);
				}

				var body = method.Body().Append(codeModel.Throw(codeModel.New(codeModel.Type(typeof(InvalidOperationException), false), Array.Empty<ICodeExpression>(), Array.Empty<AssignExpression>())));

				var attr = method.Attribute(codeModel.Type(typeof(Sql.FunctionAttribute), false))
					.Parameter(codeModel.Constant(func.FullName, true))
					.Parameter(codeModel.Identifier(nameof(Sql.FunctionAttribute.ServerSideOnly)), codeModel.Constant(true, true))
					.Parameter(codeModel.Identifier(nameof(Sql.FunctionAttribute.IsAggregate)), codeModel.Constant(true, true));
				
				var returnType = MakeType(codeModel, func.Type.CLRType, func.Type.ProviderType, settings.PreferProviderTypes, func.Type.Type.IsNullable);
				method.Returns(returnType);

				var sourceParam = codeModel.Parameter(codeModel.Type(typeof(IEnumerable<>), false, new[] { source }), codeModel.Identifier("src"), Direction.In);
				method.Parameter(sourceParam);

				if (func.Parameters.Count > 0)
				{
					var argIndexes = new ICodeExpression[func.Parameters.Count];
					for (var i = 0; i < func.Parameters.Count; i++)
					{
						var param = func.Parameters[i];
						if (param.Direction != ParameterDirection.In)
							throw new InvalidOperationException();

						argIndexes[i] = codeModel.Constant(i + 1, true);

						var parameterType = MakeType(codeModel, param.CLRType, param.ProviderType, settings.PreferProviderTypes, param.Type.IsNullable);

						parameterType = codeModel.Type(typeof(Func<,>), false, source, parameterType);
						parameterType = codeModel.Type(typeof(Expression<>), false, parameterType);

						var p = codeModel.Parameter(parameterType, codeModel.Identifier(param.ParameterName, ctxModel.Parameter, i + 1), Direction.In);
						method.Parameter(p);
						if (param.Description != null)
							(xmlComment ??= method.XmlComment()).Parameter(p.Name, param.Description);
					}

					attr.Parameter(codeModel.Identifier(nameof(Sql.FunctionAttribute.ArgIndices)), codeModel.Array(codeModel.Type(typeof(int), false), true, argIndexes, true));
				}
			}
		}

		private static void BuildScalarFunctions(
			DataContextModel ctxModel,
			IType context,
			RegionGroup regions,
			List<ScalarFunctionModel> functions,
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			ClassGroup tupleClasses,
			Func<MethodBuilder> getInitMappingSchema)
		{
			CodeBlockBuilder? initSchemaBody = null;

			foreach (var func in functions)
			{
				var region = regions.New(func.MethodName);

				var method = region.Methods(false).New(codeModel.Identifier(func.MethodName))
					.Public()
					.Static();

				XmlCommentBuilder? xmlComment = null;
				if (func.Description != null)
				{
					xmlComment = method.XmlComment().Summary(func.Description);
				}

				var body = method.Body().Append(codeModel.Throw(codeModel.New(codeModel.Type(typeof(InvalidOperationException), false), Array.Empty<ICodeExpression>(), Array.Empty<AssignExpression>())));

				method.Attribute(codeModel.Type(typeof(Sql.FunctionAttribute), false))
					.Parameter(codeModel.Constant(func.FullName, true))
					.Parameter(codeModel.Identifier(nameof(Sql.FunctionAttribute.ServerSideOnly)), codeModel.Constant(true, true));

				IType returnType;
				if (func.Type.Length == 1)
				{
					returnType = MakeType(codeModel, func.Type[0].type.CLRType, func.Type[0].type.ProviderType, settings.PreferProviderTypes, func.Type[0].type.Type.IsNullable);
				}
				else
				{
					var tuple = tupleClasses.New(codeModel.Identifier(func.TupleTypeName!)).Public().Partial();
					var props = tuple.Properties(true);
					var initializers = new AssignExpression[func.Type.Length];

					var lambdaParam = codeModel.LambdaParameter(codeModel.Identifier("tuple"));

					var idx1 = 0;
					foreach (var (type, name) in func.Type)
					{
						var property = props.New(codeModel.Identifier(name!), MakeType(codeModel, type.CLRType, type.ProviderType, settings.PreferProviderTypes, type.Type.IsNullable))
							.Public()
							.Default(true);

						initializers[idx1] = codeModel.Assign(
							property.Property.Name,
							codeModel.Cast(property.Property.Type.Type, codeModel.Index(lambdaParam.Name, codeModel.Constant(idx1, true))));

						idx1++;
					}

					var conversionLambda = codeModel
						.Lambda()
						.Parameter(lambdaParam);

					conversionLambda.Body().Append(codeModel.Return(codeModel.New(tuple.Type.Type, Array.Empty<ICodeExpression>(), initializers)));

					(initSchemaBody ??= getInitMappingSchema().Body())
						.Append(
						codeModel.Call(
							codeModel.Identifier(nameof(IDataContext.MappingSchema)),
							codeModel.Identifier(nameof(MappingSchema.SetConvertExpression)),
							new IType[]
							{
								codeModel.ArrayType(codeModel.Type(typeof(object), true), false),
								tuple.Type.Type
							},
							new ICodeExpression[] { conversionLambda.Method }));

					returnType = tuple.Type.Type;
				}
				method.Returns(returnType);

				var idx = 0;
				foreach (var param in func.Parameters)
				{
					idx++;
					if (param.Direction != ParameterDirection.In)
						throw new InvalidOperationException();

					var parameterType = MakeType(codeModel, param.CLRType, param.ProviderType, settings.PreferProviderTypes, param.Type.IsNullable);

					var p = codeModel.Parameter(parameterType, codeModel.Identifier(param.ParameterName, ctxModel.Parameter, idx), Direction.In);
					method.Parameter(p);
					if (param.Description != null)
						(xmlComment ??= method.XmlComment()).Parameter(p.Name, param.Description);
				}
			}
		}

		private static void BuildStoredProcedures(
			DataContextModel ctxModel,
			IType context,
			RegionGroup regions,
			List<StoredProcedureModel> storedProcedures,
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			bool asExtensions,
			IDictionary<EntityModel, ClassBuilder> entityTypes)
		{
			foreach (var proc in storedProcedures)
			{
				var region = regions.New(proc.MethodName);

				if (proc.SchemaError != null)
				{
					if (settings.GenerateProceduresSchemaError)
					{
						region.Pragmas().Add(codeModel.Error(proc.SchemaError));
					}

					if (settings.SkipProceduresWithSchemaErrors)
						continue;
				}

				var method = region.Methods(false).New(codeModel.Identifier(proc.MethodName))
					.Public()
					.Static();
				if (asExtensions)
					method
					.Extension();

				XmlCommentBuilder? xmlComment = null;
				if (proc.Description != null)
				{
					xmlComment = method.XmlComment().Summary(proc.Description);
				}

				var ctxParam = codeModel.Parameter(context, codeModel.Identifier("dataConnection"), Direction.In);
				method.Parameter(ctxParam);
				var body = method.Body();

				VariableExpression? parametersVar = null;
				var parameterRebinds = new List<AssignExpression>();

				var hasParameters = proc.Parameters.Count > 0 || proc.Return != null;
				if (hasParameters)
				{
					var parameterValues = new ICodeExpression[proc.Parameters.Count + (proc.Return != null ? 1 : 0)];
					parametersVar = codeModel.Variable(codeModel.Identifier("parameters"), codeModel.Type(typeof(DataParameter[]), false), true);
					var parametersArray = codeModel.Assign(
						parametersVar,
						codeModel.Array(codeModel.Type(typeof(DataParameter), false), true, parameterValues, false));
					body.Append(parametersArray);

					if (proc.Parameters.Count > 0)
					{
						for (var i = 0; i < proc.Parameters.Count; i++)
						{
							var p = proc.Parameters[i];

							var parameterType = MakeType(codeModel, p.CLRType, p.ProviderType, settings.PreferProviderTypes, p.Type.IsNullable);

							parameterValues[i] = BuildProcedureParameter(codeModel, settings, method, ref xmlComment, parametersVar, parameterRebinds,
								p.Direction,
								parameterType,
								p.Type,
								p.DbName,
								codeModel.Identifier(p.ParameterName),
								p.Description,
								i);
						}
					}

					if (proc.Return != null)
					{
						var returnType = MakeType(codeModel, proc.Return.CLRType, proc.Return.ProviderType, settings.PreferProviderTypes, proc.Return.Type.IsNullable);

						parameterValues[proc.Parameters.Count] = BuildProcedureParameter(codeModel, settings, method, ref xmlComment, parametersVar, parameterRebinds,
								ParameterDirection.Return,
								returnType,
								proc.Return.Type,
								proc.Return.DbName ?? "return",
								codeModel.Identifier(proc.Return.ParameterName),
								null,
								proc.Parameters.Count);
					}
				}

				// procedure resultset contains unnamed or duplicate column names
				// TODO: move to model
				var useCustomReader = proc.Results.Count == 1
					&& proc.Results[0].customTable != null
					&& proc.Results[0].customTable!.Columns.Select(c => c.ColumnName).Where(_ => _ != "").Distinct().Count() != proc.Results[0].customTable!.Columns.Count;

				var parameters = new ICodeExpression[2 + ((proc.Parameters.Count > 0 || proc.Return != null) ? 1 : 0) + (useCustomReader ? 1: 0)];
				parameters[0] = ctxParam.Name;
				parameters[useCustomReader ? 2 : 1] = codeModel.Constant(proc.FullName, true);
				if (proc.Parameters.Count > 0 || proc.Return != null)
					parameters[useCustomReader ? 3 : 2] = parametersVar!.Name;

				ICodeExpression? returnValue = null;
				if (proc.Results.Count == 0)
				{
					method.Returns(codeModel.Type(typeof(int), false));
					returnValue = codeModel.ExtCall(
						codeModel.Type(typeof(DataConnectionExtensions), false),
						codeModel.Identifier(nameof(DataConnectionExtensions.ExecuteProc)),
						Array.Empty<IType>(),
						parameters);
				}
				else if (proc.Results.Count > 1)
					throw new NotImplementedException();
				else
				{
					var (rs, entity) = proc.Results[0];
					IType returnType;
					if (entity != null)
					{
						returnType = entityTypes[entity].Type.Type;
					}
					else
					{
						var (resultClass, columnsMap) = BuildResultSetMapping(ctxModel, codeModel, settings, rs!, region);
						returnType = resultClass.Type.Type;

						if (useCustomReader)
						{
							// generate positional mapping
							// TODO: switch to ColumnReader.GetValue to utilize column mapping data?

							var drParam = codeModel.LambdaParameter(codeModel.Identifier("dataReader"));
							var initializers = new AssignExpression[rs!.Columns.Count];
							var lambda = codeModel.Lambda()
							.Parameter(drParam);
							parameters[1] = lambda.Method;
							lambda.Body()
								.Append(codeModel.Return(
									codeModel.New(
										codeModel.Type(resultClass.Type.Type, false),
										Array.Empty<ICodeExpression>(),
										initializers)));

							var ms = codeModel.Member(ctxParam.Name, codeModel.Identifier(nameof(DataConnection.MappingSchema)));
							for (var i = 0; i < rs.Columns.Count; i++)
							{
								var prop = columnsMap[rs.Columns[i]];
								initializers[i] = codeModel.Assign(
									columnsMap[rs.Columns[i]].Name,
									codeModel.Call(
										new TypeReference(codeModel.Type(typeof(Converter), false)),
										codeModel.Identifier(nameof(Converter.ChangeTypeTo)),
										new[] { prop.Type.Type },
										new ICodeExpression[]
										{
										codeModel.Call(drParam.Name, codeModel.Identifier(nameof(DbDataReader.GetValue)), Array.Empty<IType>(), new ICodeExpression[] { codeModel.Constant(i, true) }),
										ms
										}));
							}

							returnValue = codeModel.ExtCall(
								codeModel.Type(typeof(Enumerable), false),
								codeModel.Identifier(nameof(Enumerable.ToList)),
								Array.Empty<IType>(),
								new ICodeExpression[] 
								{
									codeModel.ExtCall(
										codeModel.Type(typeof(DataConnectionExtensions), false),
										codeModel.Identifier(nameof(DataConnectionExtensions.QueryProc)),
										Array.Empty<IType>(),
										parameters)
								});
						}
					}

					method.Returns(
						codeModel.Type(
							settings.GenerateProcedureResultAsList ? typeof(List<>) : typeof(IEnumerable<>),
							false,
							returnType));

					
					if (!useCustomReader)
					{
						returnValue = codeModel.ExtCall(
									codeModel.Type(typeof(Enumerable), false),
									codeModel.Identifier(nameof(Enumerable.ToList)),
									Array.Empty<IType>(),
									new[]
									{
										codeModel.ExtCall(
											codeModel.Type(typeof(DataConnectionExtensions), false),
											codeModel.Identifier(nameof(DataConnectionExtensions.QueryProc)),
											new[] { returnType },
											parameters)
									});
					}
				}

				if (parameterRebinds.Count > 0)
				{
					var callProcVar = codeModel.Variable(codeModel.Identifier("ret"), method.Method.ReturnType!.Type, true);
					body.Append(codeModel.Assign(callProcVar, returnValue!));
					foreach (var rebind in parameterRebinds)
						body.Append(rebind);
					body.Append(codeModel.Return(callProcVar.Name));
				}
				else
				{
					body.Append(codeModel.Return(returnValue));
				}
			}
		}

		private static ICodeExpression BuildProcedureParameter(
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			MethodBuilder method,
			ref XmlCommentBuilder? xmlComment,
			VariableExpression parametersVar,
			List<AssignExpression> parameterRebinds,
			ParameterDirection direction,
			IType type,
			DbType dbType,
			string dbName,
			CodeIdentifier name,
			string? description,
			int idx)
		{
			var ctorParams = new List<ICodeExpression>();
			var ctorInitializers = new List<AssignExpression>();

			var dir = direction == ParameterDirection.InOut
							? Direction.Ref
							: direction == ParameterDirection.Out || direction == ParameterDirection.Return
								? Direction.Out
								: Direction.In;

			var mParam = codeModel.Parameter(type, name, dir);
			method.Parameter(mParam);

			if (description != null)
			{
				(xmlComment ??= method.XmlComment())
					.Parameter(mParam.Name, description);
			}


			ctorParams.Add(codeModel.Constant(dbName, true));
			ctorParams.Add(direction == ParameterDirection.In || direction == ParameterDirection.InOut ? mParam.Name : codeModel.Null(codeModel.Type(typeof(object), true), true));
			if (dbType.DataType != null)
				ctorParams.Add(codeModel.Constant(dbType.DataType.Value, true));

			if (direction == ParameterDirection.Out)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.Direction)), codeModel.Constant(System.Data.ParameterDirection.Output, true)));
			else if (direction == ParameterDirection.InOut)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.Direction)), codeModel.Constant(System.Data.ParameterDirection.InputOutput, true)));
			else if (direction == ParameterDirection.Return)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.Direction)), codeModel.Constant(System.Data.ParameterDirection.ReturnValue, true)));

			if (dbType.Type != null && settings.GenerateProcedureParameterDbType)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.DbType)), codeModel.Constant(dbType.Type!, true)));
			if (dbType.Length != null)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.Size)), codeModel.Constant(dbType.Length.Value, true)));
			if (dbType.Precision != null)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.Precision)), codeModel.Constant(dbType.Precision.Value, true)));
			if (dbType.Scale != null)
				ctorInitializers.Add(codeModel.Assign(codeModel.Identifier(nameof(DataParameter.Scale)), codeModel.Constant(dbType.Scale.Value, true)));

			if (direction == ParameterDirection.InOut
				|| direction == ParameterDirection.Out
				|| direction == ParameterDirection.Return)
			{
				parameterRebinds.Add(
					codeModel.Assign(
						mParam.Name,
						codeModel.Call(
							new TypeReference(codeModel.Type(typeof(Converter), false)),
							codeModel.Identifier(nameof(Converter.ChangeTypeTo)),
							new[] { mParam.Type!.Type },
							new ICodeExpression[] { codeModel.Member(codeModel.Index(parametersVar.Name, codeModel.Constant(idx, true)), codeModel.Identifier(nameof(DataParameter.Value))) })));
			}

			return codeModel.New(codeModel.Type(typeof(DataParameter), false), ctorParams.ToArray(), ctorInitializers.ToArray());
		}

		private static void BuildTableFunctions(
			DataContextModel ctxModel,
			RegionGroup tableFunctions,
			List<TableFunctionModel> functions,
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			IDictionary<EntityModel, ClassBuilder> entityTypes)
		{
			foreach (var function in functions)
			{
				var funcRegion = tableFunctions.New(function.DbName.Name);

				var methodInfo = funcRegion.Fields(false).New(codeModel.Identifier(function.MethodInfoName), codeModel.Type(typeof(MethodInfo), false))
					.Private()
					.Static()
					.ReadOnly();

				var method = funcRegion.Methods(false).New(codeModel.Identifier(function.MethodName))
					.Public();

				XmlCommentBuilder? xmlComment = null;
				if (function.Description != null)
				{
					xmlComment = method.XmlComment().Summary(function.Description);
				}

				IType returnEntity;
				if (function.EntityResult != null)
					returnEntity = entityTypes[function.EntityResult].Type.Type;
				else
					returnEntity = BuildResultSetMapping(ctxModel, codeModel, settings, function.CustomResult!, funcRegion).mapping.Type.Type;

				// T4 used ITable here, but I don't see good reason to generate result as table
				var returnType = codeModel.Type(settings.TableFunctionReturnsTable ? typeof(ITable<>) : typeof(IQueryable<>), false, returnEntity);
				method.Returns(returnType);

				var parameters = new List<ICodeExpression>();
				parameters.Add(codeModel.This());
				parameters.Add(codeModel.This());
				parameters.Add(methodInfo.Field.Name);

				var defaultParameters = new List<ICodeExpression>();
				var idx = 0;
				foreach (var param in function.Parameters)
				{
					idx++;
					var paramType = MakeType(codeModel, param.CLRType, param.ProviderType, settings.PreferProviderTypes, param.Type.IsNullable);
					var parameter = codeModel.Parameter(paramType, codeModel.Identifier(param.ParameterName, ctxModel.Parameter, idx), Direction.In);
					method.Parameter(parameter);
					parameters.Add(parameter.Name);
					// TODO: target-typed default could cause errors with overloads?
					defaultParameters.Add(codeModel.Default(paramType, true));

					if (param.Description != null)
					{
						(xmlComment ??= method.XmlComment())
							.Parameter(parameter.Name, param.Description);
					}
				}

				var attr = method.Attribute(codeModel.Type(typeof(Sql.TableFunctionAttribute), false));
				// generate name always to allow refactoring by user
				//if (method.Method.Name.Name != function.DbName.Name)
					attr.Parameter(codeModel.Constant(function.DbName.Name, true));
				if (function.DbName.Schema != null)
					attr.Parameter(codeModel.Identifier(nameof(Sql.TableFunctionAttribute.Schema)), codeModel.Constant(function.DbName.Schema, true));
				if (function.DbName.Database != null)
					attr.Parameter(codeModel.Identifier(nameof(Sql.TableFunctionAttribute.Database)), codeModel.Constant(function.DbName.Database, true));
				if (function.DbName.Server != null)
					attr.Parameter(codeModel.Identifier(nameof(Sql.TableFunctionAttribute.Server)), codeModel.Constant(function.DbName.Server, true));

				method.Body()
					.Append(
						codeModel.Return(
							codeModel.ExtCall(
								codeModel.Type(typeof(DataExtensions), false),
								codeModel.Identifier(nameof(DataExtensions.GetTable)),
								new[] { returnEntity },
								parameters.ToArray())));

				var lambdaParam = codeModel.LambdaParameter(codeModel.Identifier("ctx"));
				var lambda = codeModel.Lambda()
					.Parameter(lambdaParam);
				lambda.Body().
					Append(codeModel.Return(codeModel.Call(
						lambdaParam.Name,
						method.Method.Name,
						Array.Empty<IType>(),
						defaultParameters.ToArray())));

				methodInfo.AddSetter(codeModel.Call(
						new TypeReference(codeModel.Type(typeof(MemberHelper), false)),
						codeModel.Identifier(nameof(MemberHelper.MethodOf)),
						new[] { tableFunctions.OwnerType.Type },
						new ICodeExpression[] { lambda.Method }));


				// TODO: body
				// ToDO: similar tables
			}
		}

		private static IType MakeType(CodeModelBuilder codeModel, Type type, string? providerType, bool preferProviderType, bool nullable)
		{
			if ((preferProviderType || type == typeof(object)) && providerType != null)
			{
				switch (providerType)
				{
					case "SqlByte": return codeModel.Type(typeof(SqlByte), nullable);
					case "SqlInt16": return codeModel.Type(typeof(SqlInt16), nullable);
					case "SqlInt32": return codeModel.Type(typeof(SqlInt32), nullable);
					case "SqlInt64": return codeModel.Type(typeof(SqlInt64), nullable);
					case "SqlDecimal": return codeModel.Type(typeof(SqlDecimal), nullable);
					case "SqlMoney": return codeModel.Type(typeof(SqlMoney), nullable);
					case "SqlSingle": return codeModel.Type(typeof(SqlSingle), nullable);
					case "SqlDouble": return codeModel.Type(typeof(SqlDouble), nullable);
					case "SqlBoolean": return codeModel.Type(typeof(SqlBoolean), nullable);
					case "SqlDateTime": return codeModel.Type(typeof(SqlDateTime), nullable);
					case "SqlString": return codeModel.Type(typeof(SqlString), nullable);
					case "SqlBinary": return codeModel.Type(typeof(SqlBinary), nullable);
					case "SqlGuid": return codeModel.Type(typeof(SqlGuid), nullable);
					case "SqlXml": return codeModel.Type(typeof(SqlXml), nullable);
					case "Microsoft.SqlServer.Types.SqlHierarchyId": return codeModel.Type(typeof(SqlHierarchyId), nullable);
					case "Microsoft.SqlServer.Types.SqlGeography": return codeModel.Type(typeof(SqlGeography), nullable);
					case "Microsoft.SqlServer.Types.SqlGeometry": return codeModel.Type(typeof(SqlGeometry), nullable);
				}
				throw new NotImplementedException(providerType);
			}

			return codeModel.Type(type, nullable);
		}

		private static (ClassBuilder mapping, IDictionary<ResultTableColumnModel, CodeProperty> columns) BuildResultSetMapping(
			DataContextModel ctxModel,
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			ResultTableModel model,
			CodeRegionBuilder region)
		{
			var columnsMap = new Dictionary<ResultTableColumnModel, CodeProperty>();
			var resultClass = region.Classes().New(codeModel.Identifier(model.ClassName))
					.Public()
					.Partial();
			var columns = resultClass.Properties(true);
			var pos = 0;

			var validColumNames = model.Columns.Select(c => c.ColumnName).Where(_ => _ != "").Distinct().Count() == model.Columns.Count;
			foreach (var column in model.Columns)
			{
				pos++;
				var columnType = MakeType(codeModel, column.CLRType, column.ProviderType, settings.PreferProviderTypes, column.Type.IsNullable);
				var col = columns.New(codeModel.Identifier(column.PropertyName, ctxModel.ResultColumn, pos), columnType)
					.Public()
						.Default(true);

				// for invalid column names we don't use column mappings
				if (validColumNames)
				{
					var columnAttr = col.AddAttribute(codeModel.Type(typeof(ColumnAttribute), false));
					// always add name as we can rename property (e.g. being invalid/duplicate in context)
					//if (column.ColumnName != column.PropertyName)
						columnAttr.Parameter(codeModel.Constant(column.ColumnName, true));

					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.CanBeNull)), codeModel.Constant(column.Type.IsNullable, true));

					if (settings.GenerateDbType && column.Type.Type != null)
						columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.DbType)), codeModel.Constant(column.Type.Type!, true));
					if (settings.GenerateDataType && column.Type.DataType != null)
						columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.DataType)), codeModel.Constant(column.Type.DataType!.Value, true));
					if (settings.GenerateLength && column.Type.Length != null)
						columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.Length)), codeModel.Constant(column.Type.Length!.Value, true));
					if (settings.GeneratePrecision && column.Type.Precision != null)
						columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.Precision)), codeModel.Constant(column.Type.Precision!.Value, true));
					if (settings.GenerateScale && column.Type.Scale != null)
						columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.Scale)), codeModel.Constant(column.Type.Scale!.Value, true));
				}

				columnsMap.Add(column, col.Property);
			}

			return (resultClass, columnsMap);
		}

		private static void BuildFindExtension(
			CodeModelBuilder codeModel,
			CodeGenerationSettings settings,
			ILanguageServices langServices,
			INameConverterProvider pluralizationProvider,
			Func<MethodGroup> getExtensionsBuilder,
			EntityModel entity,
			IType entityType,
			ISet<IType> customEqualityTypes)
		{
			if (entity.Columns.Any(c => c.IsPrimaryKey))
			{
				var lambdaParam = codeModel.LambdaParameter(codeModel.Identifier("t"));
				var parameters = new List<CodeParameter>();
				parameters.Add(codeModel.Parameter(codeModel.Type(typeof(ITable<>), false, entityType), codeModel.Identifier("table"), Direction.In));
				var pks = entity.Columns.Where(c => c.IsPrimaryKey);
				if (!settings.OrderFindParametersByColumnOrdinal)
					pks = pks.OrderBy(c => c.PrimaryKeyOrdinal);
				var comparisons = new List<(int pkOrdinal, ICodeExpression comparison)>();
				foreach (var pkColumn in pks)
				{
					var paramName = ContextModelBuilder.NormalizeIdentifier(
						settings.ParameterNameNormalization,
						pkColumn.PropertyName,
						langServices,
						pluralizationProvider);
					var parameterType = MakeType(codeModel, pkColumn.CLRType, pkColumn.ProviderType, settings.PreferProviderTypes, pkColumn.Type.IsNullable);

					var parameter = codeModel.Parameter(parameterType, codeModel.Identifier(paramName), Direction.In);
					parameters.Add(parameter);
					var cond = CorrectEquality(codeModel, langServices, customEqualityTypes, parameterType, codeModel.Equal(codeModel.Member(lambdaParam.Name, codeModel.Identifier(pkColumn.PropertyName)), parameter.Name));
					comparisons.Add((pkColumn.PrimaryKeyOrdinal!.Value, cond));
				}

				IEnumerable<(int pkOrdinal, ICodeExpression comparison)> orderedComparisons = comparisons;
				if (settings.OrderFindParametersByColumnOrdinal)
					orderedComparisons = orderedComparisons.OrderBy(_ => _.pkOrdinal);

				ICodeExpression filter = null!;
				foreach (var (_, cond) in orderedComparisons)
				{
					filter = filter == null ? cond : codeModel.And(filter, cond);
				}

				var findMethods = getExtensionsBuilder();

				var find = findMethods.New(codeModel.Identifier("Find"));
				foreach (var param in parameters)
					find.Parameter(param);
				var filterLambda = codeModel.Lambda()
									.Parameter(lambdaParam);
				filterLambda.Body()
					.Append(codeModel.Return(filter!));
				find
					.Public()
					.Extension()
					.Returns(codeModel.Type(entityType, true))
					.Body()
					.Append(
						codeModel.Return(
							codeModel.ExtCall(
								codeModel.Type(typeof(Queryable), false),
								codeModel.Identifier(nameof(Queryable.FirstOrDefault)),
								Array.Empty<IType>(),
								new ICodeExpression[]
								{
									parameters[0].Name,
									filterLambda.Method
								})));
			}
		}

		private static ClassBuilder BuildEntity(
			DataContextModel ctxModel,
			CodeGenerationSettings settings,
			ClassGroup classes,
			CodeModelBuilder codeModel,
			EntityModel entity,
			Dictionary<ColumnModel, CodeProperty> columns)
		{
			var cls = classes.New(codeModel.Identifier(entity.ClassName))
				.Public()
				.Partial();
			var columnsGroup = cls.Properties(true);

			var attr = cls.AddAttribute(codeModel.Type(typeof(TableAttribute), false))
				.Parameter(codeModel.Constant(entity.TableName.Name, true));
			if (entity.TableName.Schema != null)
				attr.Parameter(new CodeIdentifier(nameof(TableAttribute.Schema)), codeModel.Constant(entity.TableName.Schema, true));
			if (entity.TableName.Database != null)
				attr.Parameter(new CodeIdentifier(nameof(TableAttribute.Database)), codeModel.Constant(entity.TableName.Database, true));
			if (entity.TableName.Server != null)
				attr.Parameter(new CodeIdentifier(nameof(TableAttribute.Server)), codeModel.Constant(entity.TableName.Server, true));
			if (entity.IsView)
				attr.Parameter(new CodeIdentifier(nameof(TableAttribute.IsView)), codeModel.Constant(entity.IsView, true));

			var compositePk = entity.Columns.Count(c => c.IsPrimaryKey) > 1;
			var pos = 0;
			foreach (var column in entity.Columns)
			{
				pos++;
				var columnType = MakeType(codeModel, column.CLRType, column.ProviderType, settings.PreferProviderTypes, column.Type.IsNullable);
				var prop = columnsGroup.New(codeModel.Identifier(column.PropertyName, ctxModel.EntityColumn, pos), columnType)
					.Public()
					.Default(true);

				// compared to old T4 implementation we use only ColumnAttribute
				var columnAttr = prop.AddAttribute(codeModel.Type(typeof(ColumnAttribute), false));
				// always add name as we can rename property (e.g. being invalid/duplicate in context)
				//if (column.ColumnName != column.PropertyName)
				columnAttr.Parameter(codeModel.Constant(column.ColumnName, true));

				columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.CanBeNull)), codeModel.Constant(column.Type.IsNullable, true));

				if (settings.GenerateDbType && column.Type.Type != null)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.DbType)), codeModel.Constant(column.Type.Type, true));
				if (settings.GenerateDataType && column.Type.DataType != null)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.DataType)), codeModel.Constant(column.Type.DataType.Value, true));
				if (settings.GenerateLength && column.Type.Length != null)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.Length)), codeModel.Constant(column.Type.Length.Value, true));
				if (settings.GeneratePrecision && column.Type.Precision != null)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.Precision)), codeModel.Constant(column.Type.Precision.Value, true));
				if (settings.GenerateScale && column.Type.Scale != null)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.Scale)), codeModel.Constant(column.Type.Scale.Value, true));

				if (column.IsPrimaryKey)
				{
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.IsPrimaryKey)), codeModel.Constant(true, true));
					if (compositePk)
						columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.PrimaryKeyOrder)), codeModel.Constant(column.PrimaryKeyOrdinal!.Value, true));
				}

				if (column.IsIdentity)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.IsIdentity)), codeModel.Constant(true, true));

				if (!column.CanInsert)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.SkipOnInsert)), codeModel.Constant(true, true));
				if (!column.CanUpdate)
					columnAttr.Parameter(codeModel.Identifier(nameof(ColumnAttribute.SkipOnUpdate)), codeModel.Constant(true, true));


				if (column.Type.Type != null)
					prop.TrailingComment(column.Type.Type);

				columns.Add(column, prop.Property);

				if (column.Description != null)
					prop.XmlComment().Summary(column.Description);
			}

			if (entity.Description != null)
				cls.XmlComment().Summary(entity.Description);

			return cls;
		}

		private static void BuildAssociations(
			CodeModelBuilder codeModel,
			DataContextModel model,
			IDictionary<EntityModel, ClassBuilder> entities,
			IDictionary<ColumnModel, CodeProperty> columns,
			Func<RegionGroup> associationExtensions,
			CodeGenerationSettings settings,
			ILanguageServices langServices,
			ISet<IType> customEqualityTypes)
		{
			var entityRegions = settings.GenerateAssociations ? new Dictionary<EntityModel, PropertyGroup>() : null;
			var entityExtensionsRegions = settings.GenerateAssociationExtensions ? new Dictionary<EntityModel, MethodGroup>() : null;

			foreach (var association in model.Associations)
			{
				var fromEntityBuilder = entities[association.SourceEntity];
				var toEntityBuilder = entities[association.TargetEntity];

				if (settings.GenerateAssociations)
					BuildAssociationOnEntity(codeModel, columns, settings, entityRegions!, association, fromEntityBuilder, toEntityBuilder);

				if (settings.GenerateAssociationExtensions)
					BuildAssociationAsExtension(codeModel, columns, associationExtensions, entityExtensionsRegions!, association, fromEntityBuilder, toEntityBuilder, langServices, customEqualityTypes);
			}
		}

		private static ICodeExpression CorrectEquality(CodeModelBuilder codeModel, ILanguageServices langServices, ISet<IType> customEqualityTypes, IType type, CodeBinaryExpression expr)
		{
			if (expr.Operation == BinaryOperation.Equal && customEqualityTypes.Contains(type))
			{
				return codeModel.Cast(TypeBuilder.FromType(typeof(bool), langServices), expr);
			}

			return expr;
		}

		private static void BuildAssociationAsExtension(
			CodeModelBuilder codeModel,
			IDictionary<ColumnModel, CodeProperty> columns,
			Func<RegionGroup> associationExtensions,
			Dictionary<EntityModel, MethodGroup> entityExtensionsRegions,
			Association association, ClassBuilder fromEntityBuilder,
			ClassBuilder toEntityBuilder,
			ILanguageServices langServices,
			ISet<IType> customEqualityTypes)
		{
			if (!entityExtensionsRegions.TryGetValue(association.SourceEntity, out var fromExt))
				entityExtensionsRegions[association.SourceEntity] = fromExt = associationExtensions().New($"{fromEntityBuilder.Type.Name.Name} Associations").Methods(false);
			if (!entityExtensionsRegions.TryGetValue(association.TargetEntity, out var toExt))
				entityExtensionsRegions[association.TargetEntity] = toExt = associationExtensions().New($"{toEntityBuilder.Type.Name.Name} Associations").Methods(false);

			IType toType;
			if (association.ManyToOne)
				toType = codeModel.Type(typeof(IQueryable<>), false, fromEntityBuilder.Type.Type);
			else
				toType = codeModel.Type(fromEntityBuilder.Type.Type, association.TargetIsOptional);

			var fromType = codeModel.Type(toEntityBuilder.Type.Type, association.SourceIsOptional);
			var toResultType = codeModel.Type(fromEntityBuilder.Type.Type, association.TargetIsOptional);

			var fromExtMethod = fromExt.New(codeModel.Identifier(association.SourceMemberName))
						.Public()
						.Static()
						.Extension()
						.Returns(fromType);
			var toExtMethod = toExt.New(codeModel.Identifier(association.TargetMemberName))
						.Public()
						.Static()
						.Extension()
						.Returns(toType);

			var ctxParam = codeModel.Parameter(codeModel.Type(typeof(IDataContext), false), codeModel.Identifier("db"), Direction.In);
			var fromThisParam = codeModel.Parameter(fromEntityBuilder.Type.Type, codeModel.Identifier("obj"), Direction.In);
			var toThisParam = codeModel.Parameter(toEntityBuilder.Type.Type, codeModel.Identifier("obj"), Direction.In);

			fromExtMethod.Parameter(fromThisParam);
			fromExtMethod.Parameter(ctxParam);
			toExtMethod.Parameter(toThisParam);
			toExtMethod.Parameter(ctxParam);

			var lambdaParam = codeModel.LambdaParameter(codeModel.Identifier("c"));

			ICodeExpression? filter = null;
			for (var i = 0; i < association.TargetColumns.Count; i++)
			{
				var cond = CorrectEquality(codeModel, langServices, customEqualityTypes, columns[association.SourceColumns[i]].Type.Type, codeModel.Equal(codeModel.Member(lambdaParam.Name, codeModel.Identifier(association.TargetColumns[i].PropertyName)), codeModel.Member(fromThisParam.Name, codeModel.Identifier(association.SourceColumns[i].PropertyName))));
				filter = filter == null ? cond : codeModel.And(filter, cond);
			}
			var fromLambda = codeModel.Lambda().Parameter(lambdaParam);
			fromLambda.Body().Append(codeModel.Return(filter!));

			filter = null;
			for (var i = 0; i < association.TargetColumns.Count; i++)
			{
				var cond = CorrectEquality(codeModel, langServices, customEqualityTypes, columns[association.SourceColumns[i]].Type.Type, codeModel.Equal(codeModel.Member(lambdaParam.Name, codeModel.Identifier(association.SourceColumns[i].PropertyName)), codeModel.Member(toThisParam.Name, codeModel.Identifier(association.TargetColumns[i].PropertyName))));
				filter = filter == null ? cond : codeModel.And(filter, cond);
			}
			var toLambda = codeModel.Lambda().Parameter(lambdaParam);
			toLambda.Body().Append(codeModel.Return(filter!));

			var body = codeModel.ExtCall(
					codeModel.Type(typeof(Queryable), false),
					codeModel.Identifier(nameof(Queryable.Where)),
					Array.Empty<IType>(),
					new ICodeExpression[]
					{
						codeModel.ExtCall(
							codeModel.Type(typeof(DataExtensions), false),
							codeModel.Identifier(nameof(DataExtensions.GetTable)),
							new[] { toEntityBuilder.Type.Type },
							new ICodeExpression[] { ctxParam.Name }),
						fromLambda.Method
					});
			body = codeModel.ExtCall(
				codeModel.Type(typeof(Queryable), false),
				codeModel.Identifier(association.SourceIsOptional ? nameof(Queryable.FirstOrDefault) : nameof(Queryable.First)),
				Array.Empty<IType>(),
				new ICodeExpression[] { body });
			fromExtMethod.Body().Append(codeModel.Return(body));

			body = codeModel.ExtCall(
				codeModel.Type(typeof(Queryable), false),
				codeModel.Identifier(nameof(Queryable.Where)),
				Array.Empty<IType>(),
				new ICodeExpression[]
				{
					codeModel.ExtCall(
					codeModel.Type(typeof(DataExtensions), false),
					codeModel.Identifier(nameof(DataExtensions.GetTable)),
					new[] { fromEntityBuilder.Type.Type },
					new ICodeExpression[] { ctxParam.Name }),
					toLambda.Method
				});
			if (!association.ManyToOne)
				body = codeModel.ExtCall(codeModel.Type(typeof(Queryable), false), codeModel.Identifier(association.TargetIsOptional ? nameof(Queryable.FirstOrDefault) : nameof(Queryable.First)), Array.Empty<IType>(), new ICodeExpression[] { body });
			toExtMethod.Body().Append(codeModel.Return(body));

			fromExtMethod.XmlComment().Summary(association.KeyName);
			toExtMethod.XmlComment().Summary(association.KeyName + "_BackReference");

			var fromAttr = fromExtMethod.Attribute(codeModel.Type(typeof(AssociationAttribute), false))
				.Parameter(codeModel.Identifier(nameof(AssociationAttribute.CanBeNull)), codeModel.Constant(association.SourceIsOptional, true));
			var toAttr = toExtMethod.Attribute(codeModel.Type(typeof(AssociationAttribute), false))
				.Parameter(codeModel.Identifier(nameof(AssociationAttribute.CanBeNull)), codeModel.Constant(association.TargetIsOptional, true));

			if (association.SourceColumns.Count > 1)
			{
				var fromKey = new StringBuilder();
				var toKey = new StringBuilder();
				foreach (var col in association.SourceColumns)
				{
					if (fromKey.Length > 0)
						fromKey.Append(',');
					fromKey.Append(columns[col].Name!.Name);
				}
				foreach (var col in association.TargetColumns)
				{
					if (toKey.Length > 0)
						toKey.Append(',');
					toKey.Append(columns[col].Name!.Name);
				}

				var from = fromKey.ToString();
				var to = toKey.ToString();

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.Constant(from, true));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.Constant(to, true));

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.Constant(to, true));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.Constant(from, true));
			}
			else
			{
				var fromColumn = columns[association.SourceColumns[0]];
				var toColumn = columns[association.TargetColumns[0]];

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.NameOf(codeModel.Member(fromEntityBuilder.Type.Type, fromColumn.Name!)));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.NameOf(codeModel.Member(toEntityBuilder.Type.Type, toColumn.Name!)));

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.NameOf(codeModel.Member(toEntityBuilder.Type.Type, toColumn.Name!)));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.NameOf(codeModel.Member(fromEntityBuilder.Type.Type, fromColumn.Name!)));
			}
		}

		private static void BuildAssociationOnEntity(
			CodeModelBuilder codeModel,
			IDictionary<ColumnModel, CodeProperty> columns,
			CodeGenerationSettings settings,
			Dictionary<EntityModel, PropertyGroup> entityRegions,
			Association association,
			ClassBuilder fromEntityBuilder,
			ClassBuilder toEntityBuilder)
		{
			if (!entityRegions.TryGetValue(association.SourceEntity, out var fromRegion))
				entityRegions[association.SourceEntity] = fromRegion = fromEntityBuilder.Regions().New("Associations").Properties(false);
			if (!entityRegions.TryGetValue(association.TargetEntity, out var toRegion))
				entityRegions[association.TargetEntity] = toRegion = toEntityBuilder.Regions().New("Associations").Properties(false);
			IType toType;
			if (association.ManyToOne)
			{
				if (settings.AssociationCollectionAsArray)
				{
					toType = codeModel.ArrayType(fromEntityBuilder.Type.Type, false);
				}
				else if (settings.AssociationCollectionType != null)
				{
					toType = codeModel.Type(settings.AssociationCollectionType, false, fromEntityBuilder.Type.Type);
				}
				else
				{
					toType = codeModel.Type(typeof(IEnumerable<>), false, fromEntityBuilder.Type.Type);
				}
			}
			else
				toType = codeModel.Type(fromEntityBuilder.Type.Type, association.TargetIsOptional);
			var fromType = codeModel.Type(toEntityBuilder.Type.Type, association.SourceIsOptional);

			var fromProperty = fromRegion.New(codeModel.Identifier(association.SourceMemberName), fromType)
					.Public()
					.Default(true);
			var toProperty = toRegion.New(codeModel.Identifier(association.TargetMemberName), toType)
					.Public()
					.Default(true);

			var fromAttr = fromProperty.AddAttribute(codeModel.Type(typeof(AssociationAttribute), false))
				.Parameter(codeModel.Identifier(nameof(AssociationAttribute.CanBeNull)), codeModel.Constant(association.SourceIsOptional, true));
			fromProperty.XmlComment().Summary(association.KeyName);

			var toAttr = toProperty.AddAttribute(codeModel.Type(typeof(AssociationAttribute), false))
				.Parameter(codeModel.Identifier(nameof(AssociationAttribute.CanBeNull)), codeModel.Constant(association.TargetIsOptional, true));
			toProperty.XmlComment().Summary(association.KeyName + "_BackReference");
			var sameEntity = fromEntityBuilder == toEntityBuilder;

			if (association.SourceColumns.Count > 1)
			{
				var fromKey = new StringBuilder();
				var toKey = new StringBuilder();
				foreach (var col in association.SourceColumns)
				{
					if (fromKey.Length > 0)
						fromKey.Append(',');
					fromKey.Append(columns[col].Name!.Name);
				}
				foreach (var col in association.TargetColumns)
				{
					if (toKey.Length > 0)
						toKey.Append(',');
					toKey.Append(columns[col].Name!.Name);
				}

				var from = fromKey.ToString();
				var to = toKey.ToString();

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.Constant(from, true));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.Constant(to, true));

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.Constant(to, true));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.Constant(from, true));
			}
			else
			{
				var fromColumn = columns[association.SourceColumns[0]];
				var toColumn = columns[association.TargetColumns[0]];

				fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.NameOf(fromColumn.Name!));
				toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.ThisKey)), codeModel.NameOf(toColumn.Name!));
				if (sameEntity)
				{
					fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.NameOf(toColumn.Name!));
					toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.NameOf(fromColumn.Name!));
				}
				else
				{
					fromAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.NameOf(codeModel.Member(toEntityBuilder.Type.Type, toColumn.Name!)));
					toAttr.Parameter(codeModel.Identifier(nameof(AssociationAttribute.OtherKey)), codeModel.NameOf(codeModel.Member(fromEntityBuilder.Type.Type, fromColumn.Name!)));
				}
			}
		}

		private static IReadOnlyCollection<CodeIdentifier[]> GetImports(CodeElementNamespace ns, ILanguageServices languageServices)
		{
			var nsCollector = new NamespaceCollectorVisitor(languageServices);
			nsCollector.Visit(ns);
			return nsCollector.Namespaces;
		}
